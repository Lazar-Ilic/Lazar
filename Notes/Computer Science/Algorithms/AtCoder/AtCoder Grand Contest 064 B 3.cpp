L MST(){
    L totalweight=0;
    vector<edge> mmm(num);
    mmm[0].w=0;
    set<edge> q;
    q.insert({0,0});
    VB selected(num,false);
    for(I i=0;i<num;++i){
        if(q.empty())return -1;
		I v=q.begin()->to;
        selected[v]=true;
        totalweight+=q.begin()->w;
        q.erase(q.begin());
        if(mmm[v].to!=-1){
			//spantree[v].add(mmm[v].to);
			//spantree[mmm[v].to].add(v);
			//if(v==target)output=;
		}
        for(edge e:graph[v]){
            if(!selected[e.to] and e.w<mmm[e.to].w){
                q.erase({mmm[e.to].w,e.to});
                mmm[e.to]={e.w,v};
                q.insert({e.w,e.to});
            }
        }
    }
    return totalweight;
}

